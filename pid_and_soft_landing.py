    "# --- PID SINIFI ---\n",
    "class AltitudePID:\n",
    "    def __init__(self, Kp, Ki, Kd):\n",
    "        self.Kp = Kp\n",
    "        self.Ki = Ki\n",
    "        self.Kd = Kd\n",
    "        self.prev_error = 0\n",
    "        self.integral = 0\n",
    "        self.last_time = time.time()\n",
    "        \n",
    "    def compute(self, target_altitude, current_altitude):\n",
    "        current_time = time.time()\n",
    "        dt = current_time - self.last_time\n",
    "        if dt <= 0.0: dt = 0.01 \n",
    "        \n",
    "        error = target_altitude - current_altitude\n",
    "        P = self.Kp * error\n",
    "        self.integral += error * dt\n",
    "        self.integral = max(min(self.integral, 50), -50) # Rüzgar birikmesini engelle\n",
    "        I = self.Ki * self.integral\n",
    "        D = self.Kd * ((error - self.prev_error) / dt)\n",
    "        \n",
    "        self.prev_error = error\n",
    "        self.last_time = current_time\n",
    "        return P + I + D\n",
