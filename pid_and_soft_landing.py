    "# --- YARDIMCI FONKSİYONLAR (Öncekiyle aynı) ---\n",
    "def send_msp_command(ser, cmd, data=[]):\n",
    "    size = len(data)\n",
    "    checksum = size ^ cmd\n",
    "    for b in data: checksum ^= b\n",
    "    header = struct.pack('<3sBB', b'$M<', size, cmd)\n",
    "    ser.write(header + bytes(data) + struct.pack('<B', checksum))\n",
    "\n",
    "def send_rc_commands(ser, roll, pitch, throttle, yaw, aux1, aux2, aux3, aux4):\n",
    "    data = struct.pack('<8H', roll, pitch, throttle, yaw, aux1, aux2, aux3, aux4)\n",
    "    send_msp_command(ser, 200, list(data))\n",
    "\n",
    "def read_radio_switches(ser):\n",
    "    ser.reset_input_buffer()\n",
    "    send_msp_command(ser, 105)\n",
    "    time.sleep(0.02)\n",
    "    if ser.in_waiting >= 6:\n",
    "        if ser.read(3) == b'$M>':\n",
    "            size = ser.read(1)[0]\n",
    "            cmd = ser.read(1)[0]\n",
    "            if cmd == 105:\n",
    "                payload = ser.read(size)\n",
    "                checksum = ser.read(1)[0]\n",
    "                if len(payload) >= 12:\n",
    "                    return struct.unpack('<6H', payload[:12])\n",
    "    return None\n",
    "\n",
    "def read_altitude(ser):\n",
    "    ser.reset_input_buffer()\n",
    "    send_msp_command(ser, 109)\n",
    "    time.sleep(0.02)\n",
    "    if ser.in_waiting >= 6:\n",
    "        if ser.read(3) == b'$M>':\n",
    "            size = ser.read(1)[0]\n",
    "            cmd = ser.read(1)[0]\n",
    "            if cmd == 109:\n",
    "                payload = ser.read(size)\n",
    "                checksum = ser.read(1)[0]\n",
    "                if len(payload) >= 6:\n",
    "                    return struct.unpack('<ih', payload)[0]\n",
    "    return None\n",
